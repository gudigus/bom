package com.spring.bom.model.yeah;

public class Bookmark {
	int ucode;
	int bcode;
	String Ibtype;
	String Ibtime;

	public int getUcode() {
		return ucode;
	}

	public void setUcode(int ucode) {
		this.ucode = ucode;
	}

	public int getBcode() {
		return bcode;
	}

	public void setBcode(int bcode) {
		this.bcode = bcode;
	}

	public String getIbtype() {
		return Ibtype;
	}

	public void setIbtype(String ibtype) {
		Ibtype = ibtype;
	}

	public String getIbtime() {
		return Ibtime;
	}

	public void setIbtime(String ibtime) {
		Ibtime = ibtime;
	}

}